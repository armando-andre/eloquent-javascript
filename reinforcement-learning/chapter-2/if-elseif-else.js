// Create a variable weather and set it to "sunny".
// Write an if-else if-else block:
//     If weather is "sunny", print "It's a beautiful day!".
//     If weather is "rainy", print "Don't forget your umbrella!".
//     Otherwise (for any other weather), print "Check the weather forecast!".
// Change the value of weather to "rainy" and then to "cloudy" to see how the output changes.

let weather = "sunny";

if (weather === "sunny") {
  console.log("It's a beautiful day!");
} else if (weather === "rainy") {
  console.log("Don't forget your umbrella!");
} else {
  console.log("Check the weather forecast!");
}
